# ----- 선언 부분 -----
num1, num2 = list(map(int, input().split()))
R1 = 0
R2 =0

# ----- 문제 해결 부분 -----

R1 = (((num1 % 100) % 10) * 100) + (((num1 - (num1 // 100) * 100) // 10) * 10) + (num1 // 100)
R2 = (((num2 % 100) % 10) * 100) + (((num2 - (num2 // 100) * 100) // 10) * 10) + (num2 // 100)

if R1 > R2:
    print(R1)
else:
    print(R2)

